    public void mostrarMatriz(int matriz[][], int n) {
        DefaultTableModel tabla = (DefaultTableModel) Tabla.getModel();
        tabla.setRowCount(n);
        for (int i = 0; i < n; i++) {
            matriz[i][i] = i;
        }

    }

    public int[][] Operacion(JTable x, int n) {
        Biseccion b = new Biseccion();
        int arreglo[][] = new int[n][n];
        Xi = Double.parseDouble(XiVarible.getText());
        Xs = Double.parseDouble(XsVariable.getText());
        String cabecera[] = {"i", "Xi", "Xs", "Xr(Xi+Xs)/2", "f(Xr)", "f(Xi)", "f(Xr)*f(Xi)", "Error"};

        DefaultTableModel T = new DefaultTableModel(null, cabecera);
        for (int i = 0; i < n+1; i++) {
            String Fila[] = {"" + i, "" + Xi, "" + Xs, "" + Xr, "" + fxr, "" + fxi, "" + fxr_fxi, "" + Error};
            if (i == 0) {

                Xr = (Xi + Xs) / 2;
                fxr = ((Math.pow(Xr, 3)) + (4 * Math.pow(Xr, 2))) - 10;
                ///////////////
                fxi = ((Math.pow(Xi, 3)) + (4 * Math.pow(Xi, 2))) - 10;
                ////////////////////
                fxr_fxi = fxr * fxi;

            } else {
                if (i == 1 || i != 1) {
                    if (fxr_fxi < 0) {
                        Xs = Xr;
 
                    } else {
                        if (fxr_fxi > 0) {
                            Xi = Xr;

                        }
                    }
                    if (fxr < 0) {
                        fxi = fxr;

                    } else {
                        fxr = fxr;
                    }
                    Xr = (Xi + Xs) / 2;
                    fxr = ((Math.pow(Xr, 3)) + (4 * Math.pow(Xr, 2))) - 10;
                    ///////////////
                    fxi = ((Math.pow(Xi, 3)) + (4 * Math.pow(Xi, 2))) - 10;
                    ////////////////////
                    fxr_fxi = fxr * fxi;
                    Error = ((fxr - fxi) / fxr);
                    T.addRow(Fila);
                }
                x.setModel(T);
            }

        }
        return arreglo;
    }

