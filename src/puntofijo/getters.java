package puntofijo;

public class getters {
   private int interraciones;
   private double x;
   private double x_g;
   private double error;

    public int getInterraciones() {
        return interraciones;
    }

    public void setInterraciones(int interraciones) {
        this.interraciones = interraciones;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getX_g() {
        return x_g;
    }

    public void setX_g(double x_g) {
        this.x_g = x_g;
    }

    public double getError() {
        return error;
    }

    public void setError(double error) {
        this.error = error;
    }
}
