package newton;

public class getters {
    private int interacciones;
    private double x;
    private double normalx;
    private double derivadaX;
    private double nuevaRaiz;
    private double error;

    public int getInteracciones() {
        return interacciones;
    }

    public void setInteracciones(int interacciones) {
        this.interacciones = interacciones;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getNormalx() {
        return normalx;
    }

    public void setNormalx(double normalx) {
        this.normalx = normalx;
    }

    public double getDerivadaX() {
        return derivadaX;
    }

    public void setDerivadaX(double derivadaX) {
        this.derivadaX = derivadaX;
    }

    public double getNuevaRaiz() {
        return nuevaRaiz;
    }

    public void setNuevaRaiz(double nuevaRaiz) {
        this.nuevaRaiz = nuevaRaiz;
    }

    public double getError() {
        return error;
    }

    public void setError(double error) {
        this.error = error;
    }
    
           
}
